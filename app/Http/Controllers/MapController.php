     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\Map  $map
     * @return \Illuminate\Http\Response
     */
    public function update(Request $rq, Map $map)
    {
        $rq->validate([
            "street"=>["required"],
            "numero"=>["required"],
            "zipcode"=>["required"],
            "city"=>["required"],
        ]);

        $map->street = $rq->street;
        $map->numero = $rq->numero;
        $map->zipcode = $rq->zipcode;
        $map->city = $rq->city;
        $map->save();

        return redirect()->back()->with('success',"l'adresse a bien été modifié");
    }

